
ORG 0x40
PLUS:
LD &0x1
ST OPER_POS1
ADD NUMBER_LENGTH
ST OPER_POS2
LD NUMBER_LENGTH
ST LOOP_COUNTER
CLC
LOOPP:
LD (OPER_POS1)+
ADC (OPER_POS2)
CMP #0xA
BMI CHANGE_PL				  ;>=10
ADD #0x6
CLC
CMC
CHANGE_PL: 
AND #0xF

ST (OPER_POS2)+
LOOP LOOP_COUNTER
JUMP LOOPP
LD OPER_POS2
CALL PRINT_NUMBER
LD OPER_POINTER			;0x55
NOP
ADD NUMBER_LENGTH
ST OPER_POINTER
JUMP MAIN_LOOP
